    private void deleteAdvertismentsJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAdvertismentsJButton1ActionPerformed

        int selectedRow= adJTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row","Invalid Action", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Advertisment advertisment=  (Advertisment) adJTable1.getValueAt(selectedRow, 0);
        
        int value= JOptionPane.showConfirmDialog(null, "Are you sure to delete "+ advertisment.getAdID() +" ?", "Delete Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if(value == JOptionPane.YES_OPTION)
        {
          ArrayList<Solution> local= new ArrayList<>();
          for(Solution solution: advertiser.getSolutionCatalog().getSolutionList())
            if(solution.getAd().getAdID()==advertisment.getAdID())
                local.add(solution);
          for(Solution solution: local)
            advertiser.getSolutionCatalog().getSolutionList().remove(solution);
            advertiser.getAdDirectory().getAdList().remove(advertisment);
           populateAdTable();
        }
         deleteAdvertismentsJButton1.setEnabled(false);  
         viewDetailsJKButton2.setEnabled(false);
        // TODO add your handling code here:
    }//GEN-LAST:event_deleteAdvertismentsJButton1ActionPerformed


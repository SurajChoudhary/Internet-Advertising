    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) networkJTable1.getModel();
        model.setRowCount(0);
        
        for(Network network: system.getNetworkList()){
            Object[] row = new Object[2];
            row[0]= network;
            row[1]= network.getLocationName();
            model.addRow(row);
         }
    }

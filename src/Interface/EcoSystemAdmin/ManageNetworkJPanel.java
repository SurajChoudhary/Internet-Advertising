    private void viewNetworkJKButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewNetworkJKButton2ActionPerformed
       
         int selectedRow= networkJTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row","Invalid Action", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Network n = (Network) networkJTable1.getValueAt(selectedRow, 0);
        
        ViewUpdateNetworkJPanel viewUpdateNetworkJPanel= new ViewUpdateNetworkJPanel(userProcessContainer, n);
        userProcessContainer.add("ViewUpdateNetworkJPanel", viewUpdateNetworkJPanel);
        CardLayout layout =(CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
        // TODO add your handling code here:
    }//GEN-LAST:event_viewNetworkJKButton2ActionPerformed

    private void deleteNetworkJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteNetworkJButton1ActionPerformed

        int selectedRow= networkJTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row","Invalid Action", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Network n = (Network) networkJTable1.getValueAt(selectedRow, 0);
        
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int response =JOptionPane.showConfirmDialog(null, "Are you sure to delete "+ n.getName() +" ?", "Delete Confirmation", dialogButton, JOptionPane.WARNING_MESSAGE);
        if(response == JOptionPane.YES_OPTION)
            system.removeNetwork(n);
        populateTable();
        deleteNetworkJButton1.setEnabled(false);
        
        // TODO add your handling code here:
    }//GEN-LAST:event_deleteNetworkJButton1ActionPerformed


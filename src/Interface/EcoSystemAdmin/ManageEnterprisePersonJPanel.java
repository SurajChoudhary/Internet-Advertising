    private void deletePersonJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletePersonJButton1ActionPerformed

        Enterprise e= (Enterprise) enterpriseJComboBox1.getSelectedItem();
        int selectedRow= personJTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row","Invalid Action", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Person p = (Person) personJTable1.getValueAt(selectedRow, 0);
        
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int response =JOptionPane.showConfirmDialog(null, "Are you sure to delete "+ p.getFirstName()+" "+p.getLastName() +" ?", "Delete Confirmation", dialogButton, JOptionPane.WARNING_MESSAGE);
        if(response == JOptionPane.YES_OPTION){
           ArrayList<Employee> local = new ArrayList<>();
           ArrayList<EmployeeUserAccount> local1 = new ArrayList<>(); 
        for(Employee employee: e.getEmployeeDirectory().getEmployeeList())
           if(employee.getPerson().getPersonID()==(p.getPersonID()))
           {
            for(UserAccount ua: e.getUserAccountDirectory().getUserAccountList())
              if(((EmployeeUserAccount)ua).getEmployee().getEmpID()==(employee.getEmpID()))
                   local1.add((EmployeeUserAccount) ua);
            local.add(employee);
           }
        for(Employee employee: local) e.getEmployeeDirectory().getEmployeeList().remove(employee);
        for(UserAccount ua: local1) e.getUserAccountDirectory().getUserAccountList().remove(ua);
        e.getPersonDirectory().deletePersonbyPersonID(p.getPersonID());
        populatePersonTable(e);
        deletePersonJButton1.setEnabled(false);
        viewDetailsJButton2.setEnabled(false);
        selectedPersonJLabel6.setText("");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_deletePersonJButton1ActionPerformed


    private void populatePersonTable(Enterprise e)
    {
        DefaultTableModel model = (DefaultTableModel) personJTable1.getModel();
        model.setRowCount(0);
        for(Person person: e.getPersonDirectory().getPersonlist()){
            Object[] row = new Object[4];
            row[0]= person;
            row[1]= person.getPersonID();
            row[2]= person.getAge();
            row[3]= person.getGender();
            model.addRow(row);
         }   
    }

    private void checkForExistingAccount()
    {
        if(enterprise.getFinancialAccount().getAccountType()==null)
        {
            accountExistingInfoLabel2.setText("Account Setup Incomplete");
            informtionlabelJLabel3.setText("Select to setup Account :");
            createAccountJButton1.setEnabled(true);
        }
        else{
            detailsJPanel2.setEnabled(true);
            informtionlabelJLabel3.setText("View/Update Account Details");
            updateJButton2.setEnabled(true);
            accountTypeJComboBox1.addItem(enterprise.getFinancialAccount().getAccountType());
            accountNumberjTextField1.setText(String.valueOf(enterprise.getFinancialAccount().getAccountNumber()));
            DecimalFormat df = new DecimalFormat("#.##");
            accountBalanceJTextField1.setText(df.format(enterprise.getFinancialAccount().getAccountBalance()));
         }
    }
    

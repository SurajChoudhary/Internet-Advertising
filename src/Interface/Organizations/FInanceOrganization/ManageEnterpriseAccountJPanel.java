    private void saveJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveJButton1ActionPerformed

        if(!(accountBalanceJTextField1.getText().isEmpty()||accountNumberjTextField1.getText().isEmpty()))
        {
        if(accountNumberjTextField1.getText().length()!=10) return;
        if(accountBalanceJTextField1.getText().contains("-")||accountNumberjTextField1.getText().contains("-")
                ||accountNumberjTextField1.getText().contains("+")) 
            return;        
      
        long number;
        double amount;
         try {
             number = Long.parseLong(accountNumberjTextField1.getText());
             amount= Double.parseDouble(accountBalanceJTextField1.getText());
         } catch (Exception e) {return;
         }
        FinancialAccount account=  enterprise.getFinancialAccount();
        account.setAccountType((String) accountTypeJComboBox1.getSelectedItem());
        account.setAccountNumber(number);
        account.setAccountBalance(amount);
        JOptionPane.showMessageDialog(null, "Account Information saved Successfully","", JOptionPane.INFORMATION_MESSAGE);
        userProcessContainer.remove(this);
        ((CardLayout) userProcessContainer.getLayout()).previous(userProcessContainer);
        }else JOptionPane.showMessageDialog(null, "One or more feilds are Empty","", JOptionPane.INFORMATION_MESSAGE);

        // TODO add your handling code here:
    }//GEN-LAST:event_saveJButton1ActionPerformed


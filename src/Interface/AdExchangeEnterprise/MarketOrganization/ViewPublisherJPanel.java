    private void removePublisherJButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removePublisherJButton2ActionPerformed
        
        int selectedRow= enterpriseJTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Row","Invalid Action", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Publisher publisher = (Publisher) enterpriseJTable1.getValueAt(selectedRow, 0);

        int dialogButton = JOptionPane.YES_NO_OPTION;
        JOptionPane.showConfirmDialog(null, "Are you sure to delete "+ publisher.getName() +" ?", "Delete Confirmation", dialogButton, JOptionPane.WARNING_MESSAGE);
        if(dialogButton == JOptionPane.YES_OPTION)
        exchange.getPublisherList().remove(publisher);
        populateEnterpriseTable();
        removePublisherJButton2.setEnabled(false);

        // TODO add your handling code here:
    }//GEN-LAST:event_removePublisherJButton2ActionPerformed


    private void populateTable()
    {
        DefaultTableModel model = (DefaultTableModel) marketJTable1.getModel();
        model.setRowCount(0);
        
        for(Market market: exchange.getMarketDirectory().getMarketList()){
            Object[] object = new Object[1];
            object[0]= market;
            model.addRow(object);
         }
    }
    

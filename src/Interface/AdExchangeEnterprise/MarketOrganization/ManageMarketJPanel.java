    private void addMarketJButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addMarketJButton1ActionPerformed

        String marketType = (String) marketTypeJComboBox1.getSelectedItem();
        if(marketType == null)
        {
            JOptionPane.showMessageDialog(null, "Please select a Market","Invalid Action", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Boolean exists=  exchange.getMarketDirectory().checkMarketbyType(marketType);
        if(!exists)
        {
          Market market= exchange.getMarketDirectory().createAndAddMarket();
          market.setMarketType(marketType);
          populateTable();
          JOptionPane.showMessageDialog(null, "Market Place Created Successfully","", JOptionPane.INFORMATION_MESSAGE);
          addMarketJButton1.setEnabled(false);
        }else JOptionPane.showMessageDialog(null, "Market Already Exists in Exchange","Unable to add Market", JOptionPane.ERROR_MESSAGE);
        // TODO add your handling code here:
    }//GEN-LAST:event_addMarketJButton1ActionPerformed

    private void deleteMarketJButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteMarketJButton2ActionPerformed

        int selectedRow= marketJTable1.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a Row","Invalid Action", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Market market = (Market) marketJTable1.getValueAt(selectedRow, 0);

        int dialogButton = JOptionPane.YES_NO_OPTION;
        int response= JOptionPane.showConfirmDialog(null, "Are you sure to delete "+ market.getMarketType() +" ?", "Delete Confirmation", dialogButton, JOptionPane.WARNING_MESSAGE);
        if(response == JOptionPane.YES_OPTION)
        exchange.getMarketDirectory().getMarketList().remove(market);
        populateTable();

        // TODO add your handling code here:
    }//GEN-LAST:event_deleteMarketJButton2ActionPerformed

